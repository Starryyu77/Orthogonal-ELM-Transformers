#!/usr/bin/env python3
"""
Phase 6 Results Collector and Analyzer
Collects results from all experiments and generates summary

Usage:
    python collect_results.py
    python collect_results.py --output phase6_summary.md
"""

import json
import argparse
from pathlib import Path
from typing import Dict, List, Optional
import statistics


def load_results(result_file: Path) -> Optional[Dict]:
    """Load results from a JSON file"""
    if not result_file.exists():
        return None

    try:
        with open(result_file, "r") as f:
            results = json.load(f)

        # Find best epoch
        best_epoch = max(results, key=lambda x: x["accuracy"])

        return {
            "all_epochs": results,
            "best_accuracy": best_epoch["accuracy"],
            "best_epoch": best_epoch["epoch"],
            "best_loss": best_epoch["loss"],
            "best_f1": best_epoch.get("f1", 0),
        }
    except Exception as e:
        print(f"Error loading {result_file}: {e}")
        return None


def collect_all_results(base_dir: Path) -> Dict:
    """Collect results from all experiments"""

    datasets = ["ag_news", "sst2", "xnli", "mnli"]
    methods = ["baseline", "oelm_qk", "oelm_qk_ffn"]

    results = {}

    for dataset in datasets:
        results[dataset] = {}

        for method in methods:
            result_file = base_dir / dataset / method / "results.json"
            data = load_results(result_file)

            if data:
                results[dataset][method] = data
            else:
                results[dataset][method] = None

    return results


def calculate_improvements(results: Dict) -> Dict:
    """Calculate improvements over baseline"""

    improvements = {}

    for dataset, methods in results.items():
        if not methods.get("baseline") or not methods["baseline"]["best_accuracy"]:
            continue

        baseline_acc = methods["baseline"]["best_accuracy"]
        improvements[dataset] = {}

        for method in ["oelm_qk", "oelm_qk_ffn"]:
            if methods.get(method) and methods[method]["best_accuracy"]:
                method_acc = methods[method]["best_accuracy"]
                improvement = (
                    method_acc - baseline_acc
                ) * 100  # Convert to percentage points
                improvements[dataset][method] = {
                    "accuracy": method_acc * 100,
                    "baseline": baseline_acc * 100,
                    "improvement": improvement,
                    "relative": (method_acc / baseline_acc - 1) * 100,
                }

    return improvements


def generate_summary_table(results: Dict, improvements: Dict) -> str:
    """Generate markdown summary table"""

    lines = []
    lines.append("# Phase 6 Multi-Dataset Validation Results\n")
    lines.append("**Generated**: Auto-generated by collect_results.py\n")
    lines.append(
        "**Status**: ",
    )

    # Check completion status
    total = 12  # 4 datasets × 3 methods
    completed = sum(1 for d in results.values() for m in d.values() if m)
    lines.append(f"{completed}/{total} experiments completed\n\n")

    # Main results table
    lines.append("## Accuracy Results\n")
    lines.append(
        "| Dataset | Baseline | OELM-QK | OELM-QK-FFN | Best Method | vs Baseline |\n"
    )
    lines.append(
        "|:--------|:--------:|:-------:|:-----------:|:-----------:|:-----------:|\n"
    )

    for dataset in ["ag_news", "sst2", "xnli", "mnli"]:
        methods = results.get(dataset, {})

        baseline = methods.get("baseline")
        oelm_qk = methods.get("oelm_qk")
        oelm_qk_ffn = methods.get("oelm_qk_ffn")

        baseline_acc = f"{baseline['best_accuracy'] * 100:.2f}%" if baseline else "N/A"
        qk_acc = f"{oelm_qk['best_accuracy'] * 100:.2f}%" if oelm_qk else "N/A"
        qk_ffn_acc = (
            f"{oelm_qk_ffn['best_accuracy'] * 100:.2f}%" if oelm_qk_ffn else "N/A"
        )

        # Determine best method
        best_method = "-"
        best_improvement = "-"
        if improvements.get(dataset):
            best = max(improvements[dataset].items(), key=lambda x: x[1]["accuracy"])
            best_method = "OELM-QK" if best[0] == "oelm_qk" else "OELM-QK-FFN"
            best_improvement = f"{best[1]['improvement']:+.2f}%"

        lines.append(
            f"| {dataset:8s} | {baseline_acc:8s} | {qk_acc:7s} | {qk_ffn_acc:11s} | {best_method:11s} | {best_improvement:11s} |\n"
        )

    # Summary statistics
    lines.append("\n## Summary Statistics\n")

    if improvements:
        all_improvements = []
        for dataset_improvements in improvements.values():
            for method_data in dataset_improvements.values():
                all_improvements.append(method_data["improvement"])

        if all_improvements:
            lines.append(
                f"- **Average Improvement**: {statistics.mean(all_improvements):+.2f}%\n"
            )
            lines.append(f"- **Min Improvement**: {min(all_improvements):+.2f}%\n")
            lines.append(f"- **Max Improvement**: {max(all_improvements):+.2f}%\n")
            lines.append(
                f"- **Std Dev**: {statistics.stdev(all_improvements) if len(all_improvements) > 1 else 0:.2f}%\n"
            )

    # Detailed results
    lines.append("\n## Detailed Results by Dataset\n")

    for dataset in ["ag_news", "sst2", "xnli", "mnli"]:
        lines.append(f"\n### {dataset.upper()}\n")

        methods = results.get(dataset, {})

        for method_name, method_key in [
            ("Baseline", "baseline"),
            ("OELM-QK", "oelm_qk"),
            ("OELM-QK-FFN", "oelm_qk_ffn"),
        ]:
            data = methods.get(method_key)
            if data:
                lines.append(f"\n**{method_name}**:\n")
                lines.append(
                    f"- Best Accuracy: {data['best_accuracy'] * 100:.2f}% (Epoch {data['best_epoch']})\n"
                )
                lines.append(f"- Best Loss: {data['best_loss']:.4f}\n")
                lines.append(f"- F1 Score: {data['best_f1']:.4f}\n")

                # Training progression
                lines.append("- Training Progression:\n")
                for epoch_data in data["all_epochs"]:
                    lines.append(
                        f"  - Epoch {epoch_data['epoch']}: {epoch_data['accuracy'] * 100:.2f}% accuracy, {epoch_data['loss']:.4f} loss\n"
                    )
            else:
                lines.append(f"\n**{method_name}**: ⏳ Not completed yet\n")

    return "".join(lines)


def main():
    parser = argparse.ArgumentParser(description="Collect and analyze Phase 6 results")
    parser.add_argument(
        "--base-dir",
        type=str,
        default="/projects/LlamaFactory/OELM-Pretrain/outputs/phase6_multidata",
        help="Base directory for results",
    )
    parser.add_argument(
        "--output",
        type=str,
        default="phase6_results_summary.md",
        help="Output markdown file",
    )

    args = parser.parse_args()

    base_dir = Path(args.base_dir)

    print(f"🔍 Collecting results from: {base_dir}")

    # Collect results
    results = collect_all_results(base_dir)

    # Calculate improvements
    improvements = calculate_improvements(results)

    # Generate summary
    summary = generate_summary_table(results, improvements)

    # Save to file
    output_path = Path(args.output)
    with open(output_path, "w") as f:
        f.write(summary)

    print(f"✅ Summary saved to: {output_path}")

    # Print completion status
    total = 12
    completed = sum(1 for d in results.values() for m in d.values() if m)
    print(
        f"\n📊 Progress: {completed}/{total} experiments completed ({completed / total * 100:.1f}%)"
    )

    if completed == total:
        print("\n🎉 All experiments completed!")
    elif completed > 0:
        print(f"\n⏳ {total - completed} experiments still running or pending")
    else:
        print("\n⏳ No completed experiments yet. Run experiments first.")


if __name__ == "__main__":
    main()
